
public class MapNode {
	
	private int id;
	private int col;
	private int row;
	
	public MapNode(int id, int col, int row) {
		this.id = id;
		this.col = col;
		this.row = row;
	}
	
	public int getID() {
		return this.id;
	}
	
	public int getCol() {
		return this.col;
	}
	
	public int getRow() {
		return this.row;
	}
	

}
